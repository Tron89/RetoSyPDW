CREATE TABLE `alumno` (
  `idAlumno` int NOT NULL AUTO_INCREMENT,
  `Nombre` varchar(45) NOT NULL,
  `FechaNac` date NOT NULL,
  `Sexo` varchar(10) NOT NULL,
  `Apellidos` varchar(100) DEFAULT NULL,
  PRIMARY KEY (`idAlumno`)
) ENGINE=InnoDB AUTO_INCREMENT=1255 DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_0900_ai_ci;



CREATE TABLE `curso` (
  `idCurso` int NOT NULL AUTO_INCREMENT,
  `Curso` varchar(45) NOT NULL,
  PRIMARY KEY (`idCurso`)
) ENGINE=InnoDB AUTO_INCREMENT=17 DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_0900_ai_ci;



CREATE TABLE `alumnoasignatura` (
  `idalumnoasignatura` int NOT NULL AUTO_INCREMENT,
  `idalumno` int NOT NULL,
  `asignatura` varchar(45) DEFAULT NULL,
  `nota` int DEFAULT NULL,
  PRIMARY KEY (`idalumnoasignatura`),
  KEY `fk_alumnoasignatura_alumno_idx` (`idalumno`),
  CONSTRAINT `fk_alumnoasignatura_alumno` FOREIGN KEY (`idalumno`) REFERENCES `alumno` (`idAlumno`)
) ENGINE=InnoDB AUTO_INCREMENT=130 DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_0900_ai_ci;


CREATE TABLE `alumnocurso` (
  `idalumnocurso` int NOT NULL AUTO_INCREMENT,
  `IdCurso` int NOT NULL,
  `IdAlumno` int NOT NULL,
  `fechamatricula` date NOT NULL,
  `notamedia` float(6,2) DEFAULT NULL,
  PRIMARY KEY (`idalumnocurso`),
  KEY `fk_alumnocurso_alumno_idx` (`IdAlumno`),
  KEY `fk_alumnocurso_curso_idx` (`IdCurso`),
  CONSTRAINT `fk_alumnocurso_alumno` FOREIGN KEY (`IdAlumno`) REFERENCES `alumno` (`idAlumno`),
  CONSTRAINT `fk_alumnocurso_curso` FOREIGN KEY (`IdCurso`) REFERENCES `curso` (`idCurso`)
) ENGINE=InnoDB AUTO_INCREMENT=399 DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_0900_ai_ci;


